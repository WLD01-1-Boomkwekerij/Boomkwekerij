<p>Embleem van de nationaalsocialisten en latere Rijksvlag van nazi-Duitsland, 1935-1945
Een nazi is een aanhanger van het nationaalsocialisme. Het nationaalsocialisme is de ideologie waarop, tussen 1933 en 1945, in Duitsland een totalitaire dictatuur was gebaseerd. De grondlegger van het nationaalsocialisme was de oorspronkelijk Duits/Oostenrijkse politieke provocateur, populist en latere dictator van Duitsland Adolf Hitler, die ook aan het hoofd stond van de politieke partij waarin het nationaalsocialisme zich georganiseerd had: de Nationaalsocialistische Duitse Arbeiderspartij (NSDAP).
Onderscheid[bewerken]
Vaak worden de dictaturen van Spanje en Italië uit dezelfde tijdsperiode, die weliswaar een samenwerkingsverband met de nazi's hadden, onterecht over één kam geschoren met het nationaalsocialisme en de nazi's. De overeenkomst was dat deze landen ook dictaturen waren maar ze hadden niet de extreem racistische elementen van het nazisme. De Spaanse generaal Francisco Franco steunde op een sterk nationalistische ideologie voor zijn dictatoriale bewind over Spanje, het zogenaamde Franquisme (een verbond tussen de Spaanse katholieke kerk, het leger, de conservatieve Spaanse elite en geleid door generalissimo dictator Franco) zonder dat daar een sterk nazi-racistisch element in aanwezig was. Evenmin was de Italiaanse Benito Mussolini een nazi, maar een fascist dat evenals het Franquisme sterk nationalistisch gericht was. Het Italiaanse fascisme steunde evenals het Franquisme sterk op het nationalisme maar had ook socialistische trekjes zoals corporatistische en collectivistische elementen en stond, zoals bij de nazi's, voor een sterk land met een sterke leider. Democratie werd ook hier verworpen maar afkomst van de burgers speelde niet zoveel mee, als men maar achter de Italiaanse staat en de fascisten stond. Hitler, die in zijn beginjaren een bewonderaar van Mussolini was, heeft veel elementen van het fascisme, zoals het corporatisme, in zijn nazistische ideologie geïntegreerd. Later in de oorlog onder invloed van Hitler werden er wel racistische wetten geïntroduceerd. Mussolini werd aan het eind van de oorlog door zijn eigen fascistische partij afgezet. Na de oorlog werd het Duitse nazisme en Italiaanse fascisme verboden. In Spanje echter, dat tijdens de Tweede Wereldoorlog neutraal bleef, hield het dictatoriale bewind van Francisco Franco tot in de jaren zeventig stand.
Woordverklaring[bewerken]
Het woord 'nazi' is afkomstig van Nationalsozialist, de Duitstalige term voor een aanhanger van het nationaalsocialisme. De afkorting 'nazi' is analoog aan het iets oudere 'sozi', wat in het Duitsland van het begin van de 20e eeuw de volksnaam was voor een aanhanger van het socialisme. De term 'nazisme' is van 'nazi' afgeleid en wordt tegenwoordig gebruikt als afkorting voor nationaalsocialisme. Men gebruikt het ook als pejoratief bijvoeglijk naamwoord ('dat zijn nazistische praktijken').</p>
